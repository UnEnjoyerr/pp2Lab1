a = "Hello, World!"
print(a.upper()) #modify to all uppercase letters in string

print(a.lower()) #modify to all lowercase letters in string

print(a.replace("H", "J")) #replaces all "H" to "J" in string

print(a.split(",")) # returns ['Hello', ' World!'] 
#The split() method splits the string into substrings if it finds instances of the separator.

b = " Hello, World! "
print(b.strip()) # returns "Hello, World!", removes whitespaces at the end and start of string

